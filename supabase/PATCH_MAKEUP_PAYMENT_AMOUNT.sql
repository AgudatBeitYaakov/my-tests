-- =====================================================================
-- PATCH: סכום תשלום להשלמה — makeup_exams.amount
-- =====================================================================
-- מה זה עושה?
--   מוסיף עמודה amount (numeric, אופציונלי) — סכום כסף לתלמידה בהשלמה.
--   מוזן/נערך ליד «בתשלום» (is_paid) ברשימת ההשלמות.
--
-- מה זה לא עושה?
--   * לא מוחק שום נתון
--   * לא משנה עמודות קיימות
--   * לא ממלא ערכים אוטומטיים — רשומות קיימות יישארו עם amount = NULL
--
-- בטיחות: IF NOT EXISTS — מותר להריץ כמה פעמים.
-- איך: Supabase Dashboard → SQL Editor → Run.
-- =====================================================================

alter table public.makeup_exams
  add column if not exists amount numeric;

comment on column public.makeup_exams.amount is
  'סכום תשלום להשלמה (₪). אופציונלי — מוזן/נערך ליד «בתשלום».';

notify pgrst, 'reload schema';
