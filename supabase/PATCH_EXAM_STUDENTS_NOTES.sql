-- =====================================================================
-- PATCH: הוספת עמודת notes לטבלת exam_students
-- =====================================================================
-- מה זה עושה?
--   מוסיף עמודה אחת בלבד (notes text) לטבלת exam_students.
--
-- מה זה לא עושה?
--   * לא מוחק שום נתון
--   * לא מוחק שום טבלה
--   * לא משנה שום עמודה קיימת
--   * לא נוגע ב-RLS, ב-foreign keys, או במדיניות גישה
--
-- בטיחות:
--   הפקודה מוגנת ב-`IF NOT EXISTS` — מותר להריץ אותה כמה פעמים שרוצים,
--   ההשפעה תהיה זהה. אם העמודה כבר קיימת, ה-statement פשוט "מדלגת".
--
-- איך מריצים?
--   ב-Supabase Dashboard → SQL Editor → New query → להדביק את התוכן
--   הזה → Run.
-- =====================================================================

alter table public.exam_students
  add column if not exists notes text;

comment on column public.exam_students.notes is
  'הערות חופשיות עבור שורת תלמידה במבחן (לדוגמה: הסבר על מצב מיוחד, תיאום עם המורה וכו'')';
