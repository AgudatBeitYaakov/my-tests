-- =====================================================================
-- PATCH: הוספת עמודת «נרשמה להשלמה אוטומטי» לטבלת makeup_exams
-- =====================================================================
-- מה זה עושה?
--   מוסיף עמודה אחת בלבד: auto_registered (boolean, ברירת מחדל false).
--   כל הרשומות הקיימות יקבלו «לא» — אפשר לשנות ידנית בממשק.
--
-- מה זה לא עושה?
--   * לא מוחק שום נתון
--   * לא מוחק שום טבלה
--   * לא משנה שום עמודה קיימת
--   * לא נוגע ב-RLS, ב-foreign keys, או במדיניות גישה
--
-- בטיחות:
--   הפקודה מוגנת ב-`IF NOT EXISTS` — מותר להריץ אותה כמה פעמים שרוצים.
--
-- איך מריצים?
--   Supabase Dashboard → SQL Editor → New query → להדביק → Run.
-- =====================================================================

alter table public.makeup_exams
  add column if not exists auto_registered boolean not null default false;

comment on column public.makeup_exams.auto_registered is
  'האם התלמידה נרשמה להשלמה (סימון ידני בממשק — ברירת מחדל: לא)';
